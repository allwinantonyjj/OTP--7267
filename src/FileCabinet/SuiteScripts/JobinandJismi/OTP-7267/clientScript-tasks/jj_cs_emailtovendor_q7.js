    function fieldChanged(scriptContext) {
        let purchaseRec =  scriptContext.currentRecord;
        let sublistId = scriptContext.sublistId;
        let fieldId = scriptContext.fieldId;
        let vendor = purchaseRec.getValue({
            fieldId: 'entity'
        });
        try {
            if(sublistId == 'item' && fieldId == 'quantity'){
                let itemName = purchaseRec.getCurrentSublistText({
                    sublistId: 'item',
                    fieldId: 'item'
                });
                let line = purchaseRec.getCurrentSublistIndex({
                    sublistId: 'item'
                });
                log.debug('line:'+ line);
                let oldQuantity = purchaseRec.getSublistValue({
                    sublistId: 'item',
                    fieldId: 'quantity',
                    line: line
                });
                let newQuantity = purchaseRec.getCurrentSublistValue({
                    sublistId: 'item',
                    fieldId: 'quantity'
                });
                email.send({
                    author: -5,
                    body: 'itemName'+ itemName + ', old quantity: ' + oldQuantity + ', newQuantity: ' + newQuantity,
                    recipients: vendor,
                    subject: `The quantity updated in the PO: ${purchaseRec.id}`
                });
                log.debug('email sent');
            }
        } catch (error) {
            log.debug('error occured',error);
        }
    }

