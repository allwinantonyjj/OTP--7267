    function fieldChanged(scriptContext) {
            let salesRec = scriptContext.currentRecord;
            let fieldId = scriptContext.fieldId;
    
            try {
                if (fieldId === 'entity') {
                    let custId = salesRec.getValue({ fieldId: 'entity' });
    
                    let searchSO = search.create({
                        type: search.Type.SALES_ORDER,
                        filters: [
                            ['status', 'anyof', ['SalesOrd:A', 'SalesOrd:B', 'SalesOrd:D', 'SalesOrd:E', 'SalesOrd:F']],
                            'AND',
                            ['entity', 'is', custId],
                            'AND',
                            ['mainline', 'is', 'T']
                        ],
                        columns: ['tranid', 'entity', 'amount']
                    });
    
                    let searchResults = [];
                    searchSO.run().each(function(result) {
                        searchResults.push(result);
                        return true;
                    }); 
             
                    log.debug('searchResults:', searchResults);
    
                    let openSoNum = searchResults.length;
                    log.debug('length:', openSoNum);
    
                    salesRec.setValue({
                        fieldId: 'custbody_jj_opensonumbers',
                        value: openSoNum
                    });
                }
            } catch (error) {
                log.debug('error in fieldChanged code', error);
            }
        
    }

